    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nome,email,cursos,horario,situacao;
        nome=jTextField1.getText();
        email=jTextField2.getText();
        Logi.setNome(nome);
        Logi.setEmail(email);
        
        if(jComboBox1.getSelectedIndex() == 1){
            Logi.setCursos("Administração");
        }
        if(jComboBox1.getSelectedIndex() == 2){
            Logi.setCursos("Informática");
        }
        if(jComboBox1.getSelectedIndex() == 3){
            Logi.setCursos("Medicina");
        }
        if(jComboBox1.getSelectedIndex() == 4){
            Logi.setCursos("História");
        }
        if(jComboBox1.getSelectedIndex() == 5){
            Logi.setCursos("Engenharia");
        }
        
        
        if(jCheckBox1.isSelected() ==true){
            Logi.setHorario("Manhã");
        }
        if(jCheckBox3.isSelected() ==true){
            Logi.setHorario("Tarde");
        }
        if(jCheckBox2.isSelected() ==true){
            Logi.setHorario("Noite");
        }
        
        
        
        if(jRadioButton1.isSelected() == true){
            
            Logi.setSituacao("Cursando");
        }
          if(jRadioButton2.isSelected() == true){
            
            Logi.setSituacao("Concluido");
        }
        
        try {
            conex.Inserir(Logi);
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
          
    }//GEN-LAST:event_jButton2ActionPerformed

    
