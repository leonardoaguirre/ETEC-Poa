package Exercicio1;

public class datas 
{
		private int dia;
	    private String mes;
	    protected float ano;
	
	
	public void setDia(int novo_dia)
	{
		dia = novo_dia;
	}
	public int getDia()
	{
		return dia;
	}
	public void setMes(String novo_mes)
	{
		mes = novo_mes;
	}
	public String getMes()
	{
		return mes;
	}
	public void setAno(float novo_ano)
	{
		ano = novo_ano;
	}
	public float getAno()
	{
		return ano;
	}

}
